public class Operations{
    public int add(int firstArgument, int secondArgument){
        return firstArgument + secondArgument;
    }

    public int divide(int firstArgument, int secondArgument){
        return firstArgument/secondArgument;
    }

    public bool isFirstNumberGreaterThanSecond(int firstArgument, int secondArgument){
        return firstArgument > secondArgument;
    }
}